void	ft_sort_int_tab(int *tab, int size)
{
	int temp;
	int i;
	int j;

	temp = 0;
	i = 0;
	j = i + 1;
	while (i < size)
	{
		while (j < size)
		{
			if (tab[i] < tab[j])
			{
				temp = tab[i];
				tab[i] = tab[j];
				tab[j] = temp;
			}
			j++;
		}
		i++;
		j = 0;
	}
}
